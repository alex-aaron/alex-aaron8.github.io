function nonFriends(name, array) {
    let output = [];
  // iterating through the array
  for (let i = 0; i < array.length; i++){
    // determine if current object's name is not 'Liza'
    if (array[i].name !== name){
      let friends = array[i].friends;
      let nameExists = false; // boolean flag variable
      for (let j = 0; j < friends.length; j++){
        if (friends[j] === name){
          nameExists = true;
        }
      }
      console.log(nameExists);
      // nameExists => true or false depending on whether 'Liza' was in array
      if (nameExists === false){
        output.push(array[i].name);
      }
    }
  }
  return output;
}

